def send_message(chat_id, text, reply_markup=None):
    """Отправляет сообщение в Telegram."""
    url = f'{BASE_URL}sendMessage'
    payload = {'chat_id': chat_id, 'text': text}
    if reply_markup:
        payload['reply_markup'] = reply_markup.to_dict()
    try:
        response = requests.post(url, json=payload)
        response.raise_for_status()
    except requests.exceptions.RequestException as e:
        logger.error(f"Ошибка при отправке сообщения: {e}")


def get_class_schedule(chat_id, class_name):
    """Возвращает расписание для указанного класса."""
    logger.info(f"Запрошено расписание для класса: {class_name}")
    schedule = class_schedules.get(class_name)
    if schedule:
        if isinstance(schedule, list):  # Если schedule — это список
            schedule_text = "\n".join([f"{i + 1}. {subject}" for i, subject in enumerate(schedule)])
            send_message(chat_id, f"Расписание для {class_name.upper()}:\n{schedule_text}")
        elif isinstance(schedule, dict):  # Если schedule — это словарь
            schedule_text = ""
            for day, lessons in schedule.items():
                schedule_text += f"📅 {day.capitalize()}:\n"
                schedule_text += "\n".join([f"{i + 1}. {subject}" for i, subject in enumerate(lessons)])
                schedule_text += "\n\n"
            send_message(chat_id, f"Расписание для {class_name.upper()}:\n{schedule_text}")
        else:
            logger.error(f"Неподдерживаемый формат расписания для класса {class_name}.")
            send_message(chat_id, "Ошибка: неподдерживаемый формат расписания.")
    else:
        logger.error(f"Класс {class_name} не найден в данных.")
        send_message(chat_id, f"Класс {class_name.upper()} не найден.")


def send_class_buttons(chat_id, subject, callback_data=None):
    """Отправляет инлайн-клавиатуру для выбора класса."""
    keyboard = []
    for item_class in rooms_by_class[subject]:
        keyboard.append([InlineKeyboardButton(item_class, callback_data=f"{callback_data}_{item_class}")])

    reply_markup = InlineKeyboardMarkup(keyboard)
    send_message(chat_id, f"Выберите класс для предмета {subject.capitalize()}:", reply_markup)


def send_group_buttons(chat_id, subject, callback_data=None):
    """Отправляет инлайн-клавиатуру для выбора группы."""
    keyboard = []
    for item_class in group_rooms[subject]:
        keyboard.append([InlineKeyboardButton(item_class, callback_data=f"{callback_data}_{item_class}")])

    reply_markup = InlineKeyboardMarkup(keyboard)
    send_message(chat_id, f"Выберите группу для предмета {subject.capitalize()}:", reply_markup)


def find_cabinet(subject, class_or_group_name=None):
    """Находит кабинет для предмета."""
    if subject in fixed_rooms:
        return fixed_rooms[subject]
    elif subject in rooms_by_class and class_or_group_name in rooms_by_class[subject]:
        return rooms_by_class[subject][class_or_group_name]
    elif subject in group_rooms and class_or_group_name in group_rooms[subject]:
        return group_rooms[subject][class_or_group_name]
    return None


