def handle_admin_panel_commands(chat_id, command):
    """Обрабатывает команды админ-панели."""
    if command == "edit_bells":
        send_message(chat_id, "Введите расписание звонков в формате:\n1. 08:00-08:45\n2. 09:00-09:45\nи т.д.")
        admin_sessions[chat_id] = {"step": "edit_bells_simple"}
    elif command == "edit_schedule":
        send_message(chat_id, "Введите класс (например, 5а):")
        admin_sessions[chat_id] = {"step": "edit_schedule_class"}
    elif command == "edit_rooms":
        send_message(chat_id, "Введите новые кабинеты в формате JSON.")
        admin_sessions[chat_id] = {"step": "edit_rooms"}
    elif command == "edit_routes":
        send_message(chat_id, "Введите новые маршруты в формате JSON.")
    elif command == "add_admin":
        send_message(chat_id, "Введите логин и пароль нового администратора в формате: логин:пароль")
        admin_sessions[chat_id] = {"step": "add_admin"}
    elif command == "remove_admin":
        send_message(chat_id, "Введите логин администратора для удаления")
        admin_sessions[chat_id] = {"step": "remove_admin"}
    elif command == "view_admins":
        admin_list = "\n".join([f"- {login}" for login in admins.keys()])
        send_message(chat_id, f"Текущие администраторы:\n{admin_list}")
    elif command == "exit":
        del admin_sessions[chat_id]
        send_message(chat_id, "Выход из админ-панели.")
    else:
        send_message(chat_id, "Неизвестная команда.")


def get_updates(offset):
    """Получает сообщения от Telegram."""
    url = f'{BASE_URL}getUpdates?offset={offset}&timeout=100'
    try:
        response = requests.get(url)
        response.raise_for_status()
        return response.json() if response.ok else {'result': []}
    except requests.exceptions.RequestException as e:
        logger.error(f"Ошибка при получении обновлений: {e}")
        return {'result': []}


def main():
    """Главный цикл работы бота."""
    offset = 0
    while True:
        updates = get_updates(offset)
        for update in updates['result']:
            handle_update(update)
            offset = update['update_id'] + 1
        time.sleep(1)

if __name__ == '__main__':
    main()
