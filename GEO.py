def handle_admin_commands(chat_id, user_id, command):
    """Обрабатывает команды администратора."""
    session = admin_sessions[user_id]

    if session["step"] == "login":
        if command in admins:
            session["step"] = "password"
            session["login"] = command
            send_message(chat_id, "Введите пароль:")
        else:
            send_message(chat_id, "Неверный логин.")
            del admin_sessions[user_id]

    elif session["step"] == "password":
        if admins[session["login"]] == command:
            session["step"] = "admin_panel"
            send_admin_panel(chat_id)
        else:
            send_message(chat_id, "Неверный пароль.")
            del admin_sessions[user_id]

    elif session["step"] == "admin_panel":
        if command == '/exit':
            del admin_sessions[user_id]
            send_message(chat_id, "Вы вышли из админ-панели.")
        else:
            handle_admin_panel_commands(chat_id, command)

    elif session.get("step") == "edit_rooms":
        try:
            new_rooms = json.loads(command)
            rooms_by_class.update(new_rooms)
            save_data(data)
            send_message(chat_id, "Кабинеты успешно обновлены.")
            session["step"] = "admin_panel"
        except json.JSONDecodeError:
            send_message(chat_id, "Ошибка: некорректный JSON. Попробуйте ещё раз.")

    elif session.get("step") == "add_admin":
        try:
            login, password = command.split(":", 1)
            admins[login] = password
            save_data(data)
            send_message(chat_id, f"Новый администратор {login} успешно добавлен.")
            session["step"] = "admin_panel"
        except ValueError:
            send_message(chat_id, "Ошибка: некорректный формат. Используйте логин:пароль.")

    elif session.get("step") == "remove_admin":
        login = command.strip()
        if login in admins:
            del admins[login]
            save_data(data)
            send_message(chat_id, f"Администратор {login} удален.")
            session["step"] = "admin_panel"
        else:
            send_message(chat_id, "Администратор не найден.")
            session["step"] = "admin_panel"

    elif session.get("step") == "edit_schedule_class":
        class_name = command.lower()
        if class_name in class_schedules:
            session["class_name"] = class_name
            session["step"] = "edit_schedule_day"
            send_message(chat_id, "Введите день недели (например, понедельник):")
        else:
            send_message(chat_id, "Класс не найден.")

    elif session.get("step") == "edit_schedule_day":
        day = command.lower()
        session["day"] = day
        session["step"] = "edit_schedule_lessons"
        send_message(chat_id, "Введите уроки через запятую (например, Математика, Физика):")

    elif session.get("step") == "edit_schedule_lessons":
        lessons = [lesson.strip() for lesson in command.split(",")]
        class_name = session["class_name"]
        day = session["day"]
        class_schedules[class_name][day] = lessons
        save_data(data)
        send_message(chat_id, f"Расписание для {class_name} на {day} обновлено.")
        session["step"] = "admin_panel"

    elif session.get("step") == "edit_bells_simple":
        lines = command.strip().split("\n")
        new_bells = {}
        try:
            for line in lines:
                num, times = line.split(".", 1)
                start, end = times.strip().split("-")
                new_bells[int(num)] = (start.strip(), end.strip())
            data["bell_schedule"] = new_bells
            save_data(data)
            send_message(chat_id, "Расписание звонков обновлено.")
            session["step"] = "admin_panel"
        except:
            send_message(chat_id, "Ошибка формата. Используйте: 1. 08:00-08:45")


